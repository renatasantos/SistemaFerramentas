public class DadosFerramenta {

    String nomeFerramenta;
    String fabricante;
    String referencia;

    public void setNomeFerramenta(String n) {

        nomeFerramenta = n;
    }

    public String getNomeFerramenta() {

        return nomeFerramenta;
    }

    public void setFabricante(String f) {

        fabricante = f;
    }

    public String getFabricante() {

        return fabricante;
    }

    public void setReferencia(String ref) {

        referencia = ref;
    }

    public String getReferencia() {

        return referencia;
    }
}

